use sp_runtime::{
    traits::{IdentifyAccount, Verify},
    MultiSignature,
};

/// An index to a block.
pub type BlockNumber = u32;

/// Alias to 512-bit hash when used in the context of a transaction signature on the chain.
pub type Signature = MultiSignature;

/// Some way of identifying an account on the chain. We intentionally make it equivalent
/// to the public key of our transaction signing scheme.
pub type AccountId = <<Signature as Verify>::Signer as IdentifyAccount>::AccountId;

/// Index of a transaction in the chain.
pub type Nonce = u32;

/// A hash of some data used by the chain.
pub type Hash = sp_core::H256;

/// Opaque types. These are used by the CLI to instantiate machinery that don't need to know
/// the specifics of the runtime. They can then be made to be agnostic over specific formats
/// of data like extrinsics, allowing for them to continue syncing the network through upgrades
/// to even the core data structures.
pub mod opaque {
    use super::*;

    use polkadot_primitives::BlakeTwo256;
    use sp_runtime::generic;
    pub use sp_runtime::OpaqueExtrinsic as UncheckedExtrinsic;

    /// Opaque block header type.
    pub type Header = generic::Header<BlockNumber, BlakeTwo256>;
    /// Opaque block type.
    pub type Block = generic::Block<Header, UncheckedExtrinsic>;
    /// Opaque block identifier type.
    pub type BlockId = generic::BlockId<Block>;
}
pub use opaque::*;

/// Currency types
pub mod currency {
    pub type Balance = u128;
    pub const VFY: Balance = 1_000_000_000_000_000_000;
    pub const CENTS: Balance = VFY / 100;
    pub const MILLIS: Balance = VFY / 1_000;
    pub const HUNDREDS: Balance = 100 * VFY;
    pub const THOUSANDS: Balance = 1_000 * VFY;
    pub const MILLIONS: Balance = 1_000 * THOUSANDS;
    pub const MILLICENTS: Balance = CENTS / 1_000;
    #[cfg(not(feature = "runtime-benchmarks"))]
    pub const EXISTENTIAL_DEPOSIT: Balance = CENTS;
    #[cfg(feature = "runtime-benchmarks")]
    pub const EXISTENTIAL_DEPOSIT: Balance = MILLICENTS; // hardcoded values in staking bench
    pub const fn deposit(items: u32, bytes: u32) -> Balance {
        items as Balance * 200 * CENTS + (bytes as Balance) * 100 * MILLICENTS
    }
}
pub use currency::*;

/// This determines the average expected block time that we are targeting.
/// Blocks will be produced at a minimum duration defined by `SLOT_DURATION`.
/// `SLOT_DURATION` is picked up by `pallet_timestamp` which is in turn picked
/// up by `pallet_babe` to implement `fn slot_duration()`.
///
/// Change this to adjust the block time.
pub const MILLISECS_PER_BLOCK: u64 = 6000;

// NOTE: Currently it is not possible to change the slot duration after the chain has started.
//       Attempting to do so will brick block production.
pub const SLOT_DURATION: u64 = MILLISECS_PER_BLOCK;

// Time is measured by number of blocks.
pub const MINUTES: BlockNumber = 60_000 / (MILLISECS_PER_BLOCK as BlockNumber);
pub const HOURS: BlockNumber = MINUTES * 60;
pub const DAYS: BlockNumber = HOURS * 24;
